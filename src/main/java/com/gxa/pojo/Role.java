package com.gxa.pojo;


public class Role {

  private long roleId;
  private String rolePrivileges;
  private String roleName;
  private String roleSex;
  private long roleAge;
  private long rolePhone;
  private String roleEmail;


  public long getRoleId() {
    return roleId;
  }

  public void setRoleId(long roleId) {
    this.roleId = roleId;
  }


  public String getRolePrivileges() {
    return rolePrivileges;
  }

  public void setRolePrivileges(String rolePrivileges) {
    this.rolePrivileges = rolePrivileges;
  }


  public String getRoleName() {
    return roleName;
  }

  public void setRoleName(String roleName) {
    this.roleName = roleName;
  }


  public String getRoleSex() {
    return roleSex;
  }

  public void setRoleSex(String roleSex) {
    this.roleSex = roleSex;
  }


  public long getRoleAge() {
    return roleAge;
  }

  public void setRoleAge(long roleAge) {
    this.roleAge = roleAge;
  }


  public long getRolePhone() {
    return rolePhone;
  }

  public void setRolePhone(long rolePhone) {
    this.rolePhone = rolePhone;
  }


  public String getRoleEmail() {
    return roleEmail;
  }

  public void setRoleEmail(String roleEmail) {
    this.roleEmail = roleEmail;
  }

}
